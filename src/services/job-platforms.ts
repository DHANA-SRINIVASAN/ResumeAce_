/**
 * Represents a job posting with relevant details.
 */
export interface Job {
  /**
   * The title of the job.
   */
  title: string;
  /**
   * The company offering the job.
   */
  company: string;
  /**
   * A brief description of the job.
   */
  description: string;
  /**
   * The URL to the job posting or a search query.
   */
  url: string;
   /**
   * Optional: The platform this job suggestion is for (e.g., LinkedIn, Indeed).
   */
  platform?: string;
}

